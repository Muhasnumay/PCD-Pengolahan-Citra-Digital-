% --- Executes on button press in pushbutton5.
function pushbutton5_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton5 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
Img = handles.Img;    
    HSV = rgb2hsv(Img);
    %figure, imshow(HSV)
    
    %ekstraksi HSV
    H = HSV(:,:,1); 
    S = HSV(:,:,2); 
    V = HSV(:,:,3);

    bw = im2bw (S,60/255);
    bw = imfill(bw,'holes');
    bw = bwareaopen(bw,1000);
    %figure, imshow(bw);   
    
    R = Img(:,:,1);
    G = Img(:,:,2);
    B = Img(:,:,3);

    R(~bw) = 0;
    B(~bw) = 0;
    G(~bw) = 0;
  
    RGB = cat(3,R,G,B);
    %figure, imshow(RGB);
    
    % fitur warna HSV
    Hue = sum(sum(H))/sum(sum(bw));
    Saturation = sum(sum(S))/sum(sum(bw));
    Value = sum(sum(V))/sum(sum(bw));
    
    %luas citra
    Luas = sum(sum(bw));
    
    %fitur bentuk
    [B,L] = bwboundaries(bw,'noholes'); 
    stats = regionprops(L,'All');
    perimeter = stats.Perimeter;
    eccentricity = stats.Eccentricity;
    
    %fitur tekstur
    e = entropy(bw);
    GLCM2 = graycomatrix(bw);
    F = graycoprops(GLCM2, 'all');
    contrast = F.Contrast;
    correlation = F.Correlation;
    energi = F.Energy;
    
    input = [Hue,Saturation,Value,Luas,perimeter,eccentricity,contrast,correlation,energi];
    
    set(handles.edit7,'String',num2str(Hue))
    set(handles.edit8,'String',num2str(Saturation))
    set(handles.edit9,'String',num2str(Value))
    set(handles.edit10,'String',num2str(Luas))
    set(handles.edit11,'String',num2str(perimeter))
    set(handles.edit12,'String',num2str(eccentricity))
    set(handles.edit13,'String',num2str(contrast))
    set(handles.edit14,'String',num2str(correlation))
    set(handles.edit15,'String',num2str(energi))
        
    %tabel
    ciri_uji(1,1) = Hue;
    ciri_uji(1,2) = Saturation;
    ciri_uji(1,3) = Value;
    ciri_uji(1,4) = Luas;
    ciri_uji(1,5) = perimeter;
    ciri_uji(1,6) = eccentricity;
    ciri_uji(1,7) = contrast;
    ciri_uji(1,8) = correlation;
    ciri_uji(1,9) = energi;
    
    handles.ciri_uji = ciri_uji;
    guidata(hObject,handles)
    
